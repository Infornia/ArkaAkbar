#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	char	*tmp1;
	char	*tmp2;

	tmp1 = s1;
	tmp2 = (char *)s2;
	while (*tmp2)
		*tmp1++ = *tmp2++;
	*tmp1 = *tmp2;
	return (s1);
}
