#include "libft.h"

int		ft_isnum(char *str)
{
	int		i;

	i = 0;
	if (str[0] != 0 && (str[0] == '+' || str[0] == '-'))
		i++;
	while (str[i] != 0)
	{
		if (!ft_isdigit((int)str[i]))
			return (0);
		i++;
	}
	return (1);
}
